
package DTO;


public class vehiculo_DTO {
    
    
    private int vehModelo;
    private String vehMarca;
    private String vehColor;
    private String vehEstado;
    private int vehPrecio;
    private String datNombre;
    private String datApellido;
    private int datUsuId;
    

    public int getDatUsuId() {
        return datUsuId;
    }

    public void setDatUsuId(int datUsuId) {
        this.datUsuId = datUsuId;
    }

    
    public String getDatNombre() {
        return datNombre;
    }

    public void setDatNombre(String datNombre) {
        this.datNombre = datNombre;
    }

    public String getDatApellido() {
        return datApellido;
    }

    public void setDatApellido(String datApellido) {
        this.datApellido = datApellido;
    }
   


    public int getVehModelo() {
        return vehModelo;
    }

    public void setVehModelo(int vehModelo) {
        this.vehModelo = vehModelo;
    }

    public String getVehMarca() {
        return vehMarca;
    }

    public void setVehMarca(String vehMarca) {
        this.vehMarca = vehMarca;
    }

    public String getVehColor() {
        return vehColor;
    }

    public void setVehColor(String vehColor) {
        this.vehColor = vehColor;
    }

    public String getVehEstado() {
        return vehEstado;
    }

    public void setVehEstado(String vehEstado) {
        this.vehEstado = vehEstado;
    }
    
    public int getVehPrecio() {
        return vehPrecio;
    }

    public void setVehPrecio(int vehPrecio) {
        this.vehPrecio = vehPrecio;
    }
    
    
   
}
